		<h2>The Speakers</h2>
		
		<p>One of the things I did notice, which I should have noticed right off is the severe lacks in bass. It has enough to get your through some explosions and what not, but don't expect to hear any of those deep bass effects. It's not noticibly <em>bad</em> but it's not amazing. It'll get your through a game of Fruit Ninja with no problems, but any sort of music is noticibly lacking in the bass department.</p>
		
		<p>This headset however makes up in crisp treble, in fact most of the sounds that come out of these headphones are pitch perfect if you were to ask me. The quality and clearity of this headset is probably the best I've heard so far.</p>
		
		<p>These headphones get loud, a lot louder than I would have suspected an over-ear headphone to be. At times I was yelling into my microphone over the gunshots and carnage that was happening around me. This headset helps when you're trying to hear what your lower voiced friends are saying over Skype and allows for your voice to come across the best as possible.</p>
		
		<p>However just because the speakers can get loud doesn't mean that your buddy is going to be hearing all that through the mic. Sure there are times where you might catch a slight glimse of environment or if your friend has speakers going you can hear some of what they're listening to but, Logitech has done an amazing job at cancelling out all other unwanted background noise. I want to say that 97% (remember kids statistics are made up) of the time, all I heard was <a href="../profiles/demonphoenix37.php">demonphoenix37</a> speaking. This being said I had a game playing at 100% volume while on a Skype call and they didn't notice any noise from the game bleeding into the mic even though it was pouring out of my headphones.</p>
		
	</div>
	<div class="content">
	
		<h2>The Mic</h2>
		
		<p>So, I've already mentioned how  the mic does a good job at dropping out the unwanted noise, now I want to talk more about the mic.</p>
		
		<p>It delivers voice over the internet spectularly, I love this mic, it makes our voices sound awesome. I really have no complaints about the mic. It bends to however you need it to be and rolls back out of your way when you don't need it.</p>
		
	</div>
	<div class="content">
	
		<h2>Other Stuff</h2>
		
		<p>So this headset comes with the option of using a USB way of inserting the headphone and mic part into your computer or you can just plug those cables into your machine directly through the ports if you have them. If you don't have those ports that USB port can be really handy, personally I just tossed it in my drawer.</p>

		<p>The chord on this thing is about 6 ft. which helps getting that headset from your rig to your noggin or help you keep chatting while you reach for something within the room.</p>
	
