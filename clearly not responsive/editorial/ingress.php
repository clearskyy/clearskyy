	<h1><a href="http://en.wikipedia.org/wiki/Ingress_(game)">Ingress</a></h1>
	
	<p id="date">January 21, 2012</p>
	
	<p>So there is this new game going about the internet for Android devices, and if you haven't heard about it yet, you should probably make an effort to give it a look up and maybe <a href="http://youtu.be/92rYjlxqypM">watch the video about it.</a></p>
	
	<p>I'm writting this knowing this has been around for a while, but I really didn't want to write about something I didn't get a chance to experience myself.</p>
	
	<h2>what the hell is Ingress</h2>
	
	<p>In the game Ingress, the world isn't what it seems, scattered around the Earth is this exotic material or XM that is just seemingly scattered about for no apparent reason. My first thoughts, <a href="http://moarpowah.com/wp-content/uploads/2012/12/aliens-meme.jpg">aliens</a>. It's not really explained (or maybe it is I just didn't feel like looking it up) why this material is scattered about, but this material; with the use of "Portals" has the potiential to control how us humans think. Basically it's material that has the potiential to shape how people around it think. Mind control substances, the stronger the portals the strong the influence of this glowy stuff. </p>
	
	<p>The goal is to get as much XM and Portals so that you can control more of the population. The Portals can be linked up and when you have three linked together, it forms like a blanket that covers the area within the three linked portals. </p>
	
	<p>The more leveled up you are, the stronger portals you can make, and the longer your links can be so that you cover more area. </p>
	
	<p>It's a giant game of Dominance. </p>
	
	<h2>Getting the code</h2>
	
	<p>So, this game is a closed beta at the time I'm writting this, so the only way you can get a chance to play this thing is either: scour Google+ in hopes of finding someone with a code, having a buddy give you a code, or you somehow manage to impress the 4-5 main people dishing out invites to the beta. You can download the game off of <a href="https://play.google.com/store/apps/details?id=com.nianticproject.ingress">Google Play</a> but it's locked unless you input the proper code. So basically you have a useless game on your phone that screams to be unlocked and explored.</p>
	
	<p>I've already got a invite code after about a month or two. What I did was I tried creating propaganda and funny images using the faction logos and the Ingress logo itself. That didn't work out so well, but I did see a lot of different people get a code that way. They really seem to like really interesting and creative ways of re-creating the Ingress logo. </p>
	

	<p>In the end what I think what got me an invite was my reply on twitter post made by <a href="https://twitter.com/NianticProject">@NianticProject</a> though the email never stated why I got invited, just that I was. I was estatic. I'm still level 1 since my invite but I'm almost to level 2, I just have to get a data plan so I can start collecting portals like pokemon.</p>
	
	<h2>Playing Ingress </h2>
	
	<p>That being said, collecting portals is easier said than done. Firstly you have to plant 8 resonators after you hack a portal, and you can only hack and plant a portal every 5 minutes, so you better get cozy if you get near one of these things because if you don't have multiple accounts or friends helping you, you're going to be sitting there a while (approx 45 minutes), unless of course there are portals down the street and you walk up and down the street a couple times. That sounds good if it were summer but it's January and it's -16°C (3°F) outside and no one wants to stand in that, unless of course you got some crazy death wish. I would hope that people would be sensible enough to use their vehicles and sit in heat if they're choosing to venture out in this cold.</p>
	
	<p>The second reason this isn't so easy is that there are 2 factions:  <a href="https://groups.google.com/forum/#!forum/ingress-enlightened">The Enlightened</a>, and <a href="https://groups.google.com/forum/#!forum/ingress-resistance">The Resistance</a>. This means that at any given time, anyone from anywhere from another faction can come along and just straight up destory your work. That means if you don't babysit your portals, you might as well kiss your advances behind. As frustrating as this might seem; it adds a layer of difficulty and the competition makes it all the more fun. Still I wish that guy from several towns over didn't make effort to come to my town to destroy my resonators, dick.</p>
	
	<p>It creates community. So now instead of pretending to fight to save the planet with your friends on a board game, you can now do this with people all over the world using your mobile device. Which is good because we all need to get out of the house some time, and what better way to do that than by pretending to be an agent bent on either advancing technology or saving humanity and keeping our traditional way of life intact. Its augmented reality, and who doesn't love that sort of thing?</p>
	
	<p>Basically what it comes down to is that these two factions are trying to "control" as many people as possible by linking these portals. Honestly if you're not playing the game, you have zero idea this is happening and from what I can tell this is an andriod device only game that you have to actively fight to get into until the offical release some time this year(?).</p>
	
	<p>In conclusion, it's actually kind of fun to run around this imaginary land where we're fighting for the control of the world using Google Maps with an overlay. I'm interested to see how this game evolved over time and I'll see you out there fellow Ingress players!</p>
	
