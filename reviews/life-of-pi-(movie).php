				</div>
			</div>
			
			
			<div class="content-clearskyy"><p>So my girlfriend has been reading this book and talks about it now and again. At any rate, we come across this movie at FYE and decide to get it, why not, sounds like a good story and she seems pretty excited about it.</p>
<p>It says on the box that it's the next <a href="http://www.avatarmovie.com/" target="_blank">Avatar</a>, which is a ballsy thing to say, but whatever, I'm in, I like pretty movies.</p>
<p>Holy fuck is it pretty.</p>
<p>I want to say that this movie looks <em>better</em> than Avatar, but that's just me. I mean there isn't any explosions and crazy aliens running around fucking people who are shooting at them with futuristic weapons but damn the scenes in this movie are amazing. My favorite being the scene the one at night with all the jelly fish, you'll see what I mean, its pretty awesome.</p>
<p>All the colors scream into your eyes, the saturation or contrast or whatever it is doing is&nbsp;phenomenal. Every scene, every&nbsp;environment, the objects, even the&nbsp;people&nbsp;in this movie have some astounding color quality to them. The contrasts are amazing, and really add this flavor of emphasis that makes everything that much more rich and&nbsp;enjoyable.</p>
<p>however, that's enough about how awesome it looks.</p>
<p><strong>The story</strong> is really&nbsp;engaging&nbsp;and is told completely in flashback through the narrator who is telling this story to a journalist. This great story about how this one&nbsp;spiritual man's faith was tested over and over again while he grows older and eventually winds up on a boat with some pretty intense animals.</p>
<p>I won't spoil anything but I will say that even though you're wondering how you can watch a movie that sounds a lot of&nbsp;<a href="http://www.imdb.com/title/tt0162222/" target="_blank">Castaway</a>&nbsp;meets&nbsp;<a href="http://www.imdb.com/title/tt0351283/?ref_=sr_2" target="_blank">Madagascar</a>, the story <em>will</em> keep you watching if the imagery isn't enough for you.</p>
<p>I mean this is one of the few movies my girlfriend hasn't fallen asleep through, that in itself is a miracle. The suspense and action and random acts of nature will keep you entertained as you watch Pi struggle and endure his journey back home.</p>
<p>I watched this one blu-ray, so if you don't happen to have the means to do so, I suggest you steal a nice tv and blu-ray player from someone you love and sit down and watch this movie, it'll be worth the petty larceny. I would suggest a surround system but I didn't happen to have one and the movie was fine.</p>
<p><strong>The sound</strong> score was amazing as well. As I'm writing this I'm listening to the music that is&nbsp;auto-playing&nbsp;on <a href="http://www.lifeofpimovie.com" target="_blank">lifeofpimovie.com</a> and it's pretty epic, really making this review all the better to write. hmm, yes quite enjoyable. There really isn't anything missing from this movie, except perhaps&nbsp;recognizable&nbsp;actors, but even that might have been out of place for this type of narrative and honestly wouldn't have fit well with the plot anyways.</p>
<p>The lack of any real&nbsp;recognizable&nbsp;actor really makes you interested in finding out who these characters are, and what their story really is, rather than, oh there goes so-and-so doing what so-and-so&nbsp;always&nbsp;does in every movie. This creates more emotional pull towards them since you're learning how to feel about these characters through the thoughts and feelings the narrator.</p>
<p>All in all it was a great movie and I suggest you give it a watch even if you are like me and didn't read the book.</p>
<p>Rating: 9.5 / 10, highly recommended, watch / buy.</p><p><em>written</em>: by <a href="../profiles/clearskyy.php" target="_blank">clearskyy</a></p>
