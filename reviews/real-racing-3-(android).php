			<div class="content-mrfixit"><p>Hello there,</p>
<p>I've written some satirical articles in the past and figured it'd be time to write a serious one this time. I recently downloaded "Real Racing 3" from the google play store. The screen shots looked too good to be true. Honestly, this is the best graphics I've seen on my Nexus 7 and it's FREE!&nbsp;<br /><br />Gameplay:<br /><br />Real Racing 3 starts you off with two car choices; the Nissan Silvia and the Ford Focus RS. I chose the Silvia because "hektic skidz yo." And from there you participate in multiple race styles to build cash and driver level. I compare this game to a light weight but very impressive alternative to Forza.&nbsp;<br /><br />Controls:<br /><br />In the regular modes such as "head-to-head, autocross, endurance, etc" you steer by turning the tablet and braking with either thumb. Now, I don't usually care for turning the whole device to steer but this game executes it beautifully. You get full lock to lock with just a little bit of movement and it isn't overly sensitive. There are breaking, turning and traction control assists but I recommend turning them off to get full control of the car.&nbsp;</p>
<p>Visuals:</p>
<p>Now this game could be great even without the visuals but "GOT DAYUUUM" they look amazing. How does this run on the Tegra 3? No effing clue. I have experience some frame rate drop during endurance races with lots of cars on screen but it was far from unplayable. Overall, this is the best looking game I have played on the Nexus 7.&nbsp;</p>
<p>Free but not so free?:</p>
<p>Ok, so this game is free to download. I have completed 3 "seasons" so far. And I haven't had to stick a dime into the game. However, EA has implement some features to encourage you to dump money into the game. You have in game credits and tokens. To upgrade your car/ perform maintenance you have to wait an absurd amount of time (1-5 minutes) or use tokens to "get it now." You start off with 20 tokens. I'm down to 2 now. So I will have to purchase a few shortly. Tokens cost anywhere from 10 cents to 19 cents depending on how many you purchase.</p>
<p>Overall:</p>
<p>I give this game a 7/10. As for a racing game on the nexus I give it a 9/10.</p>
