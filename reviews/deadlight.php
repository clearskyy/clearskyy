			<div class="content-nexussloth"><p>I think I've been too heavy handed with the swearing on my reviews, so I'm gonna tone it the fuck down with this one and actually give a review.
<br>
<h2>WOAH, SIDESCROLLER (read: nothing new here)</h2><br>
In this game, you play as some hobo-looking dude who starts with no weapons and probably smells terrible. So far, so shitty. Oh, wait. There are zombies. 
<br>
There's not a lot to this game, really. You run a LOT. The Running Man had less running. Dr. Who has less running. This whole game is running around as fast as you can and being inefficient as fuck.
<br>
But I digress.
<br>
<h2>LET'S TALK ABOUT THE STORY... AND THINGS</h2><br>
Alright, so this game has a pretty... basic...
FUCK IT.
This game is just another letdown of a zombie game. There's no buildup or apparent goal in the story, you're thrown into a world of terrible explanation, and the only reason to progress the story is to figure out what the in the hell is going on.
<br>
So there's zombies. And survivors. And tons and tons of flashbacks.
<br>
The character development is awful, and so is the dialogue. "Oh. I miss my wife." Your main character says this completely nonchalantly at one point, and this is supposed to be him in his most depressed state. So not only is the dialogue fucking terrible, the voice actors are ass-tastic.
<br>
Long story short? The story is crap.
<br>

<h2>THE GAME AND GAMEPLAY, BRAH</h2><br>

Alright, so I mentioned the running; that aside, I thought the controls and the feel of the movement weren't half-bad. Moving through this game made sense, and the WAY you move isn't super responsive: you're character has weight, it takes a little time to jump, etc.. But that's definitely a plus, if you want to feel some semblance of realism.<br>

Beyond that, the graphics are pretty solid for a sidescroller, 
and the environments are actually pretty neat. There's some good moments where you're like "Wow. I'm glad I spent five bucks on this steam game."
<br>
The combat is completely useless, though: you find an axe, which SOUNDS epic, but it's not.
<br>
Oh, how it is not.
<br>
See, you have a stamina bar in this game. So you can swing your axe about four times before your hobo lungs start collapsing in on their filthy selves, leaving you no ability to sprint away because (surprise!) you need stamina to run.
<br>
Sure, there are guns later, but the time spent aiming your weapon and having to shoot zombies twice in the head with damn near every weapon is pretty inefficient and sort of a letdown.
<br>
<h2>ALRIGHT, SO LET'S WRAP THIS UP</h2><br>
I can honestly see why some people would be into this game: zombies, parkour, nice environments, and some casual sidescrolling. But really, there's so many more reasons as to why people would just be disappointed in this game. <br>

But if you really feel the need to be a hobo with an axe who can sprint up walls and experience terrible combat, then this game is for you.</p><p><em>written</em>: by <a href="../profiles/NexusSloth.php">NexusSloth</a></p>
