		<p>They did give me something helpful advice though, which I will share with you if you have an&nbsp;Android&nbsp;phone;</p>
		
		<p>To make sure your phone is on your network, do the following:</p>
			<ol>
				<li>Go to settings &gt; More (4 option, which is under Data Usage) &gt; Mobile Network &gt; Network Operators</li>
				<li>Select your network</li>
				<li>This should lock your phone to your network to&nbsp;avoid&nbsp;roaming or phone to jumping to other nearby networks.</li>
